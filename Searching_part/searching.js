document.getElementById('searchBar').addEventListener('keyup', (e) => {
	const searchData = e.target.value.toLowerCase();
	if (searchData === 'shirt') {
		const shirtData = product.filter(item => item.type.toLowerCase() === 'shirt');
		displayItem(shirtData);
	} else if (searchData === 't-shirt') {
		const tshirtData = product.filter(item => item.type.toLowerCase() === 't-shirt');
		displayItem(tshirtData);
	} else if (searchData === 'jeanes') {
		const jeansData = product.filter(item => item.type.toLowerCase() === 'jeanes');
		displayItem(jeansData);
	} else if (searchData === 'lower') {
		const lowerData = product.filter(item => item.type.toLowerCase() === 'lower');
		displayItem(lowerData);
	} else if (searchData === "top's") {
		const topsData = product.filter(item => item.type.toLowerCase() === "top's");
		displayItem(topsData);
	} else if (searchData === "kurta") {
		const kurtaData = product.filter(item => item.type.toLowerCase() === "kurta");
		displayItem(kurtaData);
	} else if (searchData === "saree's") {
		const sareesData = product.filter(item => item.type.toLowerCase() === "saree's");
		displayItem(sareesData);
	} else if (searchData === "grown") {
		const grownData = product.filter(item => item.type.toLowerCase() === "grown");
		displayItem(grownData);
	} else if (searchData === "boysjeanes") {
		const smallkurtaData = product.filter(item => item.type.toLowerCase() === "boysjeanes");
		displayItem(smallkurtaData);
	} else if (searchData === "boyscloth") {
		const whiteData = product.filter(item => item.type.toLowerCase() === "boyscloth");
		displayItem(whiteData);
	}
	else if (searchData === "girlscloth") {
		const whiteData = product.filter(item => item.type.toLowerCase() === "girlscloth");
		displayItem(whiteData);
	}
});


const displayItem = (items) => {
	document.getElementById('root').innerHTML = items.map((item) => {
		var { image, rating, price } = item;
		return (
			`<div class='box'>
                <div class='img-box'>
                    <img class='images' src=${image}></img>
                </div> 
                <div class='bottom'>
                    <p>Rating: ${rating}</p>
                    <h2>$ ${price}.00</h2>
                    <button class="add-to-cart">Add to cart</button>
                </div>
            </div>`
		)
	}).join('')
};






const main_card=document.querySelector(".cart-part")
document.addEventListener("click", function (event) {
	if (event.target.classList.contains("add-to-cart")) {
		const main_box= event.target.closest('.box');
		const imgElement =main_box.querySelector('.images');
		const ratingElement =main_box.querySelector('.bottom p');
		const priceElement =main_box.querySelector('.bottom h2');

		const image = imgElement.src;
		const rating = ratingElement.textContent.trim().replace("Rating: ", "");
		const price = priceElement.textContent.trim().replace("$", "").replace(".00", "");
		console.log("Image:", image);
		console.log("Rating:", rating);
		console.log("Price:", price);


		const box = document.createElement('div');
		box.classList.add('box');

		const imgBox = document.createElement('div');
		imgBox.classList.add('img-box');

		const img = document.createElement('img');
		img.classList.add('images');
		img.src = image;
		img.alt = 'Product Image';

		const bottom = document.createElement('div');
		bottom.classList.add('bottom');

		const ratingParagraph = document.createElement('p');
		ratingParagraph.textContent = 'Rating: ' + rating;

		const priceHeading = document.createElement('h2');
		priceHeading.textContent = '$ ' + price + '.00';

		const addToCartButton = document.createElement('button');
        addToCartButton.classList.add('remove-add-cart');
        addToCartButton.textContent = 'Remove from cart';

        addToCartButton.addEventListener('click', function() {
            box.remove();
        });

		imgBox.appendChild(img);
		bottom.appendChild(ratingParagraph);
		bottom.appendChild(priceHeading);
		bottom.appendChild(addToCartButton);
		box.appendChild(imgBox);
		box.appendChild(bottom);
		main_card.appendChild(box)
	}
});














